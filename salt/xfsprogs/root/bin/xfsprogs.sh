#!/usr/bin/env bash
#-------------------------------------------------------------------------------
devs=( $( ls -1d /dev/{s,{u,x,}v}d{b..z}1 2>/dev/null ) )
#-------------------------------------------------------------------------------
for dev in ${devs[@]}
do
    if ! mount                                                                 \
       | egrep "^${dev}.*type xfs "
    then
        mkfs.xfs -i size=1024 ${dev}
    fi
done
#-------------------------------------------------------------------------------
